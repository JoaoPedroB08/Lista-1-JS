// 1) Crie um programa onde o usuário digite um número e informe se é positivo ou negativo.

numeroUsuario = Number(prompt('Digite um número que irei lhe dizer se é positivo ou negativo: '))

if(numeroUsuario >= 0){

    alert('Este número que digitou é positivo')

}else if(numeroUsuario < 0){

    alert('Este número é negativo')

}else{

    alert('Vc provavelmente não digitou um número')

}













